"""função range para gerar números de 1 a 10;
utilizar loop for para multiplicar por 5
impressão simples do numero multiplicado por 5;"""
print("Multiplicar números de 1 a 10 por 5:")
for numero in range(1, 11):
    print(numero * 5)

